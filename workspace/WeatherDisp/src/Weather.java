
public class Weather {
	boolean isCloudy;
	boolean isRainy;
	
	public boolean isCloudy() {
		return isCloudy;
	}
	public void setCloudy(boolean isCloudy) {
		this.isCloudy = isCloudy;
	}
	public boolean isRainy() {
		return isRainy;
	}
	public void setRainy(boolean isRainy) {
		this.isRainy = isRainy;
	}
	
	
	
	

}
